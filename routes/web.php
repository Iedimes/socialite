// Rutas de autenticación de Google
Route::get('login/google', 'App\Http\Controllers\Auth\LoginController@redirectToGoogle')
    ->name('redirectToGoogle');

Route::get('login/google/callback', 'App\Http\Controllers\Auth\LoginController@handleGoogleCallback')
    ->name('handleGoogleCallback');

// Ruta de inicio
Route::get('/', 'App\Http\Controllers\HomeController@index')->name('home');

// Ruta de bienvenida que verifica si el usuario está autenticado
Route::get('/home', function () {
    if (Auth::guard('web')->check()) {
        return app(App\Http\Controllers\HomeController::class)->bienvenida();
    }

    return redirect()->route('home'); // Redirige a la página de inicio si no está autenticado
})->name('home2');

// Ruta de cierre de sesión
Route::post('logout', 'App\Http\Controllers\Auth\LoginController@logout')->name('logout');

/* Auto-generated admin routes */
Route::middleware(['auth:' . config('admin-auth.defaults.guard'), 'admin'])->group(static function () {
    Route::prefix('admin')->namespace('App\Http\Controllers\Admin')->name('admin/')->group(static function() {
        Route::prefix('admin-users')->name('admin-users/')->group(static function() {
            Route::get('/',                                             'AdminUsersController@index')->name('index');
            Route::get('/create',                                       'AdminUsersController@create')->name('create');
            Route::post('/',                                            'AdminUsersController@store')->name('store');
            Route::get('/{adminUser}/impersonal-login',                 'AdminUsersController@impersonalLogin')->name('impersonal-login');
            Route::get('/{adminUser}/edit',                             'AdminUsersController@edit')->name('edit');
            Route::post('/{adminUser}',                                 'AdminUsersController@update')->name('update');
            Route::delete('/{adminUser}',                               'AdminUsersController@destroy')->name('destroy');
            Route::get('/{adminUser}/resend-activation',                'AdminUsersController@resendActivationEmail')->name('resendActivationEmail');
        });
    });
});

